package com.kodilla.good.patterns.chalenges.Food2Door;

public class Client {
    private String name;
    private String lastname;

    public Client(String name, String lastname) {
        this.name = name;
        this.lastname = lastname;
    }

    public String getName() {
        return name;
    }

    public String getLastname() {
        return lastname;
    }
}
